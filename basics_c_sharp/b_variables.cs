#region 1. Variables

// Une variable est un espace mémoire réservé pour contenir une donnée ou un ensemble de données (tableau, objet, ... )

// pour déclarer une variable, on précise d'abord le type de donnée, son nom = sa valeur;

// les variables ' simples ' peuvent être de nombreux types différents :

string message = " - Hello World"; // Toujours des guillemets pour les variables de type string.
int entier = 15;
uint = 30;
double = 50.000;
float = 3,14;

// etc ...


// Les variables ' complexes ' sont donc des ensembles de données, comme par exemple :

int[] tableau_entier = new int[entier];

Personne personne = new Personne;


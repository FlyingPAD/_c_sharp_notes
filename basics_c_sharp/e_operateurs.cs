// Le language C# prend en charge différents opérateurs, tous regroupés en plusieurs catégories :

// Arithmétiques : +, -, *, /, %

int result = (10 + (20 / 2) - (5 * 2)) % 2;

Console.WriteLine();
Console.WriteLine("   - Opérateurs arithmétiques : ");
Console.WriteLine();
Console.WriteLine($"   (10 + (20 / 2) - (5 * 2)) % 2 = {result}");
Console.ReadLine();

